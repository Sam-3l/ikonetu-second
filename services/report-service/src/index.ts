// POST /api/v1/reports/founder-score/:venture_id (PDF)
app.post(
  '/api/v1/reports/founder-score/:venture_id',
  rateLimiter({ max: 10 }),
  authenticate,
  async (req, res, next) => {
    try {
      const { venture_id } = req.params;

      // Verify ownership
      const venture = await db('ventures').where({ id: venture_id }).first();
      if (!venture) throw new NotFoundError('Venture');

      const isOwner = venture.user_id === req.user!.id;
      const isAdmin = req.user!.role === 'super_admin';
      if (!isOwner && !isAdmin) throw new AppError('Access denied', 403, 'forbidden');

      // Deduct R09 credit (billing-service handles this — here we just log it)
      await db('revenue_events').insert({
        stream_id: 'R09',
        user_id: req.user!.id,
        amount: 40,
        currency: 'GBP',
        event_type: 'report.founder_score.generated',
        metadata: JSON.stringify({ ventureId: venture_id }),
      });

      // Build PDF
      const doc = new PDFDocument({
        size: 'A4',
        margins: { top: 50, bottom: 50, left: 50, right: 50 },
        bufferPages: true,
        info: {
          Title: `IkonetU Score Report — ${venture.name}`,
          Author: 'IkonetU Technology Limited',
          Creator: 'IkonetU Report Service',
        },
      });

      res.setHeader('Content-Type', 'application/pdf');
      res.setHeader('Content-Disposition', `attachment; filename="ikonetu-score-report-${venture_id}.pdf"`);
      doc.pipe(res);

      await buildFounderScorePDF(venture_id, doc);
      doc.end();
    } catch (err) { next(err); }
  }
);

// POST /api/v1/reports/lender-portfolio (Excel)
app.post(
  '/api/v1/reports/lender-portfolio',
  rateLimiter({ max: 5 }),
  authenticate,
  async (req, res, next) => {
    try {
      if (req.user!.role !== 'lender' && req.user!.role !== 'super_admin') {
        throw new AppError('Lender role required', 403, 'forbidden');
      }

      const lenderProfile = await db('lender_profiles').where({ user_id: req.user!.id }).first();
      if (!lenderProfile && req.user!.role !== 'super_admin') {
        throw new AppError('Complete your lender profile first', 400, 'no-profile');
      }

      const targetLenderId = req.body.lenderId || lenderProfile?.id;

      await db('revenue_events').insert({
        stream_id: 'R09',
        user_id: req.user!.id,
        amount: 120,
        currency: 'GBP',
        event_type: 'report.lender_portfolio.generated',
        metadata: JSON.stringify({ lenderId: targetLenderId }),
      });

      const workbook = new ExcelJS.Workbook();
      workbook.creator = 'IkonetU Technology Limited';
      workbook.created = new Date();
      workbook.properties.date1904 = false;

      await buildLenderPortfolioExcel(targetLenderId, workbook);

      res.setHeader('Content-Type', 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet');
      res.setHeader('Content-Disposition', `attachment; filename="ikonetu-portfolio-${new Date().toISOString().slice(0,10)}.xlsx"`);

      await workbook.xlsx.write(res);
      res.end();
    } catch (err) { next(err); }
  }
);

// GET /api/v1/reports/available — what reports can this user generate
app.get(
  '/api/v1/reports/available',
  rateLimiter(),
  authenticate,
  async (req, res, next) => {
    try {
      const reports: { id: string; name: string; format: string; price: number; currency: string }[] = [];

      if (['founder', 'super_admin'].includes(req.user!.role)) {
        reports.push({
          id: 'founder-score', name: 'Founder Score Report', format: 'PDF',
          price: 40, currency: 'GBP',
        });
      }

      if (['lender', 'super_admin'].includes(req.user!.role)) {
        reports.push({
          id: 'lender-portfolio', name: 'Portfolio Monitoring Report', format: 'Excel',
          price: 120, currency: 'GBP',
        });
      }

      if (['investor', 'super_admin'].includes(req.user!.role)) {
        reports.push({
          id: 'investor-pipeline', name: 'Investor Pipeline Report', format: 'PDF',
          price: 80, currency: 'GBP',
        });
      }

      if (req.user!.role === 'super_admin') {
        reports.push({
          id: 'platform-intelligence', name: 'Platform Intelligence Report', format: 'Excel',
          price: 800, currency: 'GBP',
        });
      }

      res.json({ reports });
    } catch (err) { next(err); }
  }
);

