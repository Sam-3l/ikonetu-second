// R09-2: LENDER PORTFOLIO REPORT — Excel
// ════════════════════════════════════════════════════════════

async function buildLenderPortfolioExcel(
  lenderId: string,
  workbook: ExcelJS.Workbook
): Promise<void> {

  const lenderProfile = await db('lender_profiles').where({ id: lenderId }).first();
  if (!lenderProfile) throw new NotFoundError('Lender profile');

  const portfolio = await db('lender_portfolios as lp')
    .join('ventures as v', 'lp.venture_id', 'v.id')
    .join('users as u', 'v.user_id', 'u.id')
    .leftJoin('scores as s', function () {
      this.on('s.venture_id', 'v.id').andOn(db.raw('s.is_current = true'));
    })
    .leftJoin('bankability_scores as b', function () {
      this.on('b.venture_id', 'v.id');
    })
    .where({ 'lp.lender_id': lenderId })
    .select(
      'v.name', 'v.sector', 'v.country', 'v.stage',
      's.total_score', 's.tier', 's.confidence_pct', 's.scored_at',
      'b.total_score as bankability',
      'lp.status', 'lp.disbursed_amount', 'lp.disbursed_currency', 'lp.disbursed_at',
      'lp.monitoring_active',
    )
    .orderBy('s.total_score', 'desc');

  // Sheet 1: Portfolio Overview
  const sheet1 = workbook.addWorksheet('Portfolio Overview');
  sheet1.columns = [
    { header: 'Venture',          key: 'name',             width: 28 },
    { header: 'Sector',           key: 'sector',           width: 18 },
    { header: 'Country',          key: 'country',          width: 10 },
    { header: 'Stage',            key: 'stage',            width: 12 },
    { header: 'IkonetU Score',    key: 'total_score',      width: 14 },
    { header: 'Tier',             key: 'tier',             width: 12 },
    { header: 'Confidence %',     key: 'confidence_pct',   width: 14 },
    { header: 'Bankability Score',key: 'bankability',      width: 16 },
    { header: 'Status',           key: 'status',           width: 14 },
    { header: 'Disbursed',        key: 'disbursed_amount', width: 14 },
    { header: 'Currency',         key: 'disbursed_currency', width: 10 },
    { header: 'Disbursed Date',   key: 'disbursed_at',     width: 16 },
    { header: 'Score Date',       key: 'scored_at',        width: 16 },
    { header: 'Monitoring',       key: 'monitoring_active', width: 12 },
  ];

  // Style header row
  sheet1.getRow(1).font = { bold: true, color: { argb: 'FFFFFFFF' } };
  sheet1.getRow(1).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF0A0A0A' } };
  sheet1.getRow(1).height = 20;

  for (const row of portfolio) {
    const excelRow = sheet1.addRow({
      ...row,
      total_score:      row.total_score ?? '—',
      confidence_pct:   row.confidence_pct ? `${Math.round(row.confidence_pct)}%` : '—',
      bankability:      row.bankability ? Math.round(row.bankability) : '—',
      disbursed_amount: row.disbursed_amount ?? '—',
      disbursed_at:     row.disbursed_at ? formatDate(row.disbursed_at) : '—',
      scored_at:        row.scored_at ? formatDate(row.scored_at) : '—',
      monitoring_active: row.monitoring_active ? 'Yes' : 'No',
    });

    // Colour-code tier column
    const tierCell = excelRow.getCell('tier');
    const colors: Record<string, string> = {
      EARLY: 'FF94A3B8', RISING: 'FFF59E0B', INVESTABLE: 'FF10B981', ELITE: 'FFC9900C',
    };
    if (row.tier && colors[row.tier]) {
      tierCell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: colors[row.tier] } };
      tierCell.font = { color: { argb: 'FFFFFFFF' }, bold: true };
    }
  }

  // Auto-filter
  sheet1.autoFilter = { from: 'A1', to: 'N1' };
  sheet1.freezeRows(1);

  // Sheet 2: Score History
  const sheet2 = workbook.addWorksheet('Score History');
  sheet2.columns = [
    { header: 'Venture',    key: 'venture',   width: 28 },
    { header: 'Date',       key: 'date',      width: 16 },
    { header: 'Score',      key: 'score',     width: 10 },
    { header: 'Tier',       key: 'tier',      width: 12 },
    { header: 'Confidence', key: 'confidence', width: 12 },
  ];

  sheet2.getRow(1).font = { bold: true, color: { argb: 'FFFFFFFF' } };
  sheet2.getRow(1).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF0A0A0A' } };

  for (const p of portfolio) {
    const history = await db('score_history').where({ venture_id: p.name }).orderBy('snapshot_date');
    for (const h of history) {
      sheet2.addRow({
        venture:    p.name,
        date:       formatDate(h.snapshot_date),
        score:      h.total_score,
        tier:       h.tier,
        confidence: `${Math.round(h.confidence_pct)}%`,
      });
    }
  }

  sheet2.autoFilter = { from: 'A1', to: 'E1' };
  sheet2.freezeRows(1);

  // Sheet 3: Summary stats
  const sheet3 = workbook.addWorksheet('Summary');
  sheet3.getColumn(1).width = 30;
  sheet3.getColumn(2).width = 20;

  const statsData = [
    ['Report Generated', formatDate(new Date())],
    ['Portfolio Size', portfolio.length],
    ['Active Loans', portfolio.filter(p => p.status === 'active_loan').length],
    ['Monitoring Only', portfolio.filter(p => p.status === 'monitoring').length],
    ['Avg IkonetU Score', portfolio.length > 0 ? Math.round(portfolio.reduce((s, p) => s + (p.total_score || 0), 0) / portfolio.length) : '—'],
    ['Avg Bankability', portfolio.filter(p => p.bankability).length > 0 ? Math.round(portfolio.reduce((s, p) => s + (p.bankability || 0), 0) / portfolio.filter(p => p.bankability).length) : '—'],
    ['Tier ELITE',      portfolio.filter(p => p.tier === 'ELITE').length],
    ['Tier INVESTABLE', portfolio.filter(p => p.tier === 'INVESTABLE').length],
    ['Tier RISING',     portfolio.filter(p => p.tier === 'RISING').length],
    ['Tier EARLY',      portfolio.filter(p => p.tier === 'EARLY').length],
  ];

  for (const [label, value] of statsData) {
    const row = sheet3.addRow([label, value]);
    row.getCell(1).font = { bold: true };
  }
}

