// R09-1: FOUNDER SCORE REPORT — PDF
// ════════════════════════════════════════════════════════════

async function buildFounderScorePDF(
  ventureId: string,
  doc: PDFKit.PDFDocument
): Promise<void> {

  const venture = await db('ventures').where({ id: ventureId }).first();
  if (!venture) throw new NotFoundError('Venture');

  const score = await db('scores').where({ venture_id: ventureId, is_current: true }).first();
  const breakdowns = score ? await db('score_breakdowns').where({ score_id: score.id }).orderBy('weighted_score', 'desc') : [];
  const history = await db('score_history').where({ venture_id: ventureId }).orderBy('snapshot_date', 'asc').limit(12);
  const documents = await db('venture_documents').where({ venture_id: ventureId }).whereNull('deleted_at');
  const bankability = await db('bankability_scores').where({ venture_id: ventureId }).orderBy('scored_at', 'desc').first();

  const tier = score?.tier ?? 'EARLY';
  const tierLabels: Record<string, string> = {
    EARLY: 'Early Stage', RISING: 'Rising', INVESTABLE: 'Investable', ELITE: 'Elite',
  };

  // ── Page 1: Cover ────────────────────────────────────────
  doc.rect(0, 0, doc.page.width, 200).fill(IKONETU_BLACK);

  doc.fillColor('#F5C842').fontSize(28).font('Helvetica-Bold')
    .text('IkonetU', 50, 40);
  doc.fillColor('#888888').fontSize(11).font('Helvetica')
    .text('African Founder Scoring Platform', 50, 75);

  doc.fillColor('#FFFFFF').fontSize(22).font('Helvetica-Bold')
    .text('Founder Score Report', 50, 110);
  doc.fillColor('#AAAAAA').fontSize(12).font('Helvetica')
    .text(`${venture.name}`, 50, 140)
    .text(`Generated: ${formatDate(new Date())}`, 50, 160);

  // Score badge
  const badgeX = doc.page.width - 170;
  doc.roundedRect(badgeX, 60, 120, 120, 8).fill(TIER_COLORS[tier] + '30');
  doc.fillColor(TIER_COLORS[tier]).fontSize(48).font('Helvetica-Bold')
    .text(String(score?.total_score ?? 0), badgeX + 10, 80, { width: 100, align: 'center' });
  doc.fontSize(11)
    .text(tierLabels[tier], badgeX + 10, 130, { width: 100, align: 'center' });
  doc.fillColor('#AAAAAA').fontSize(9)
    .text('out of 1000', badgeX + 10, 148, { width: 100, align: 'center' });

  doc.fillColor(IKONETU_BLACK);

  // Disclaimer
  doc.y = 220;
  doc.rect(50, doc.y, doc.page.width - 100, 44).fill('#F8F7F4');
  doc.fillColor('#555555').fontSize(9).font('Helvetica')
    .text(
      'IMPORTANT: This report is generated by IkonetU Technology Limited for informational purposes only. ' +
      'It does not constitute financial advice, a credit assessment, or an investment recommendation. ' +
      'IkonetU scores reflect data available at the time of calculation and may not be current.',
      58, doc.y + 8, { width: doc.page.width - 116 }
    );

  // ── Page 1 continued: Summary ────────────────────────────
  doc.y = 290;
  doc.fillColor(IKONETU_BLACK).fontSize(14).font('Helvetica-Bold').text('Score Summary', 50, doc.y);
  doc.y += 24;

  const summaryItems = [
    ['Venture',           venture.name],
    ['Sector',            venture.sector || '—'],
    ['Country',           venture.country || '—'],
    ['Stage',             venture.stage || '—'],
    ['IkonetU Score',     score ? `${score.total_score} / 1000` : 'Not yet calculated'],
    ['Tier',              tierLabels[tier]],
    ['Confidence',        score ? `${Math.round(score.confidence_pct)}%` : '—'],
    ['Bankability Score', bankability ? `${Math.round(bankability.total_score)} / 100` : '—'],
    ['Report Date',       formatDate(new Date())],
    ['Calculated',        score ? formatDate(score.scored_at) : '—'],
  ];

  for (const [label, value] of summaryItems) {
    doc.rect(50, doc.y, doc.page.width - 100, 22).fill(summaryItems.indexOf([label, value]) % 2 === 0 ? '#FFFFFF' : '#F8F7F4');
    doc.fillColor('#888888').fontSize(10).font('Helvetica').text(label, 56, doc.y + 6, { width: 150 });
    doc.fillColor(IKONETU_BLACK).font('Helvetica-Bold').text(String(value), 210, doc.y + 6, { width: 300 });
    doc.y += 22;
  }

  // ── Page 2: Category breakdown ───────────────────────────
  doc.addPage();

  doc.fillColor(IKONETU_BLACK).fontSize(14).font('Helvetica-Bold').text('Score Breakdown by Category', 50, 50);
  doc.fontSize(10).font('Helvetica').fillColor('#888888')
    .text('Higher verification tier signals produce greater score impact. Self-declared data (Tier 4) contributes 60% of a signal\'s raw value; government-verified data (Tier 1) contributes 100%.', 50, 72, { width: doc.page.width - 100 });

  doc.y = 110;

  // Category bars
  for (const b of breakdowns) {
    const pct = b.max_possible > 0 ? (b.weighted_score / b.max_possible) : 0;
    const barWidth = (doc.page.width - 200) * pct;

    doc.fillColor(IKONETU_BLACK).fontSize(11).font('Helvetica-Bold')
      .text(b.category.charAt(0).toUpperCase() + b.category.slice(1), 50, doc.y);

    doc.fillColor('#888888').fontSize(9).font('Helvetica')
      .text(`${Math.round(b.weighted_score)} / ${b.max_possible} points  ·  ${b.signals_found} signals  ·  ${b.signals_verified} verified`, 200, doc.y + 2);

    doc.y += 18;

    // Track background
    doc.rect(50, doc.y, doc.page.width - 200, 10).fill('#E8E6E0');
    // Fill
    if (barWidth > 0) {
      doc.rect(50, doc.y, barWidth, 10).fill(
        pct >= 0.8 ? '#10B981' : pct >= 0.5 ? '#F59E0B' : '#94A3B8'
      );
    }
    // Percentage label
    doc.fillColor('#555555').fontSize(9).font('Helvetica')
      .text(`${Math.round(pct * 100)}%`, doc.page.width - 140, doc.y);

    doc.y += 22;
  }

  // ── Page 3: Score history chart (text-based) ─────────────
  if (history.length > 0) {
    doc.addPage();
    doc.fillColor(IKONETU_BLACK).fontSize(14).font('Helvetica-Bold').text('Score Progression', 50, 50);

    doc.y = 80;
    doc.rect(50, doc.y, doc.page.width - 100, 24).fill('#F8F7F4');
    doc.fillColor('#888888').fontSize(9).font('Helvetica').text('Date', 56, doc.y + 8, { width: 100 });
    doc.text('Score', 160, doc.y + 8, { width: 60 });
    doc.text('Tier', 230, doc.y + 8, { width: 100 });
    doc.text('Change', 340, doc.y + 8, { width: 80 });
    doc.y += 24;

    for (let i = 0; i < history.length; i++) {
      const h = history[i];
      const prev = history[i - 1];
      const change = prev ? h.total_score - prev.total_score : null;

      doc.rect(50, doc.y, doc.page.width - 100, 20).fill(i % 2 === 0 ? '#FFFFFF' : '#F8F7F4');
      doc.fillColor('#555555').fontSize(9).font('Helvetica').text(formatDate(h.snapshot_date), 56, doc.y + 5, { width: 100 });
      doc.fillColor(IKONETU_BLACK).font('Helvetica-Bold').text(String(h.total_score), 160, doc.y + 5, { width: 60 });
      doc.fillColor(TIER_COLORS[h.tier as keyof typeof TIER_COLORS] || '#888888').font('Helvetica').text(h.tier, 230, doc.y + 5, { width: 100 });
      if (change !== null) {
        doc.fillColor(change > 0 ? '#10B981' : change < 0 ? '#EF4444' : '#888888')
          .text(`${change > 0 ? '+' : ''}${change}`, 340, doc.y + 5, { width: 80 });
      }
      doc.y += 20;
    }
  }

  // ── Page 4: Documents verified ───────────────────────────
  if (documents.length > 0) {
    doc.addPage();
    doc.fillColor(IKONETU_BLACK).fontSize(14).font('Helvetica-Bold').text('Verification Evidence', 50, 50);

    doc.y = 80;
    for (const d of documents) {
      const verified = d.verified;
      doc.rect(50, doc.y, doc.page.width - 100, 22).fill(verified ? '#ECFDF5' : '#F8F7F4');
      doc.fillColor(verified ? '#065F46' : '#888888').fontSize(10)
        .text(verified ? '✓' : '○', 56, doc.y + 6, { width: 20 });
      doc.fillColor(IKONETU_BLACK).font('Helvetica')
        .text(d.document_type.replace(/_/g, ' ').replace(/\b\w/g, (c: string) => c.toUpperCase()), 80, doc.y + 6, { width: 200 });
      if (verified) {
        doc.fillColor('#888888').fontSize(9)
          .text(`Tier ${d.verification_tier} · ${formatDate(d.verified_at)}`, 290, doc.y + 7, { width: 200 });
      } else {
        doc.fillColor('#AAAAAA').fontSize(9).text('Pending verification', 290, doc.y + 7);
      }
      doc.y += 22;
    }
  }

  // ── Footer on all pages ──────────────────────────────────
  const pageCount = doc.bufferedPageRange();
  for (let i = pageCount.start; i < pageCount.start + pageCount.count; i++) {
    doc.switchToPage(i);
    doc.rect(0, doc.page.height - 36, doc.page.width, 36).fill('#F8F7F4');
    doc.fillColor('#AAAAAA').fontSize(8).font('Helvetica')
      .text(
        `IkonetU Technology Limited · Registered in England and Wales · customer.service@ikonetu.com · Page ${i + 1} of ${pageCount.count}`,
        50, doc.page.height - 24, { align: 'center', width: doc.page.width - 100 }
      );
  }
}

